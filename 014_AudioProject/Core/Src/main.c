// Override fungsi _write agar printf bisa mengirim karakter ke UART
int _write(int fd, char* ptr, int len) {
  // Hanya kirim ke UART jika tujuannya STDOUT atau STDERR
  if (fd == 1 || fd == 2) {
    for (int i = 0; i < len; i++) {
      // Jika ketemu \n, kirim \r dulu agar rapi di serial monitor
      if (ptr[i] == '\n') {
        uint8_t cr = '\r';
        HAL_UART_Transmit(&huart1, &cr, 1, 10);
      }
      // Kirim karakter aslinya
      HAL_UART_Transmit(&huart1, (uint8_t *)&ptr[i], 1, 10);
    }
    return len;
  }
  return -1;
}

