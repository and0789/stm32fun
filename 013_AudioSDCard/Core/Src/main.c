// Print to UART
int _write(int fd, char* ptr, int len) {
  if (fd == 1 || fd == 2) {
    for (int i = 0; i < len; i++) {
      if (ptr[i] == '\n') {
        uint8_t cr = '\r';
        HAL_UART_Transmit(&huart1, &cr, 1, 10);
      }
      HAL_UART_Transmit(&huart1, (uint8_t *)&ptr[i], 1, 10);
    }
    return len;
  }
  return -1;
}

