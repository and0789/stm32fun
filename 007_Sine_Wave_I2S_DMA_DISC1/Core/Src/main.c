int _write(int file, char *ptr, int len) {
  for (int i = 0; i < len; ++i) {
    ITM_SendChar(*ptr++);
  }
  return len;
}

void process_buffer(int16_t *buffer) {

  dma_buffer_processing = 1;

  for (int i = 0; i < I2S_DMA_BUFFER_SAMPLES; ++i) { // Two samples left/right per step

    buffer[i * 2] = OUTPUT_MID * amplification[0] * arm_sin_f32(angle[0]);      // Left
    buffer[i * 2 + 1] = OUTPUT_MID * amplification[1] * arm_sin_f32(angle[1]);  // Right

    angle[0] += angle_change[0];
    angle[1] += angle_change[1];

    if (angle[0] > TAU)
      angle[0] -= TAU;
    if (angle[1] > TAU)
      angle[1] -= TAU;
  }

  dma_buffer_processing = 0;

}

void HAL_I2S_TxCpltCallback(I2S_HandleTypeDef *hi2s) {
  if (dma_buffer_processing) {
    ++dma_overflow; // Still processing previous buffer
  } else {
    process_buffer(&i2s_dma_buffer[2 * I2S_DMA_BUFFER_SAMPLES]); // Second half
    ++i2c_cb;
  }
}

void HAL_I2S_TxHalfCpltCallback(I2S_HandleTypeDef *hi2s) {
  if (dma_buffer_processing) {
    ++dma_overflow; // Still processing previous buffer
  } else {
    process_buffer(&i2s_dma_buffer[0]);
    ++i2c_hcb;
  }

}

